const FRACTIONS = {
  '¼': 0.25,
  '½': 0.5,
  '¾': 0.75,
  '⅛': 0.125,
  '⅜': 0.375,
  '⅝': 0.625,
  '⅞': 0.875,
};

function normalizeName(name) {
  return name
    .toLowerCase()
    .replace(/\b(jr|sr)\.?\b/g, '')
    .replace(/[^a-z0-9]/g, '');
}

function parseMaybeFloat(value) {
  if (!value || value === '-' || value === '—') return null;
  const numeric = Number.parseFloat(String(value).trim());
  return Number.isFinite(numeric) ? numeric : null;
}

function parseBroadJump(value) {
  if (!value || value === '-' || value === '—') return null;
  const text = String(value).trim();
  if (/^\d+$/.test(text)) return Number.parseInt(text, 10);
  const match = text.match(/^(\d+)-(\d+)$/);
  if (!match) return null;
  return (Number.parseInt(match[1], 10) * 12) + Number.parseInt(match[2], 10);
}

function parseHeightInches(value) {
  if (!value) return null;
  const text = String(value).replace(/\*/g, '').replace(/\s+/g, '').trim();
  const [feetText, inchText = '0'] = text.split('-');
  const feet = Number.parseInt(feetText, 10);
  if (!Number.isFinite(feet)) return null;

  let inches = 0;
  let remainder = inchText;
  const wholeMatch = remainder.match(/^\d+/);
  if (wholeMatch) {
    inches += Number.parseInt(wholeMatch[0], 10);
    remainder = remainder.slice(wholeMatch[0].length);
  }
  for (const [glyph, amount] of Object.entries(FRACTIONS)) {
    if (remainder.includes(glyph)) inches += amount;
  }
  return Math.round(((feet * 12) + inches) * 1000) / 1000;
}

function parseMeasurements(raw) {
  const map = new Map();
  raw.trim().split('\n').forEach((line) => {
    const [name, height, weight] = line.split('|');
    map.set(normalizeName(name), {
      heightIn: parseHeightInches(height),
      weightLbs: parseMaybeFloat(weight),
    });
  });
  return map;
}

function parseTesting(raw) {
  const map = new Map();
  raw.trim().split('\n').forEach((line) => {
    const [name, fortyYard, vertical, broadJump, threeCone, shuttle, benchPress] = line.split('|');
    map.set(normalizeName(name), {
      fortyYard: parseMaybeFloat(fortyYard),
      vertical: parseMaybeFloat(vertical),
      broadJump: parseBroadJump(broadJump),
      threeCone: parseMaybeFloat(threeCone),
      shuttle: parseMaybeFloat(shuttle),
      benchPress: parseMaybeFloat(benchPress),
    });
  });
  return map;
}

const measurementsByPlayer = parseMeasurements(MEASUREMENTS_2026);
const testingByPlayer = parseTesting(TESTING_2026);
const inviteesByName = new Set(COMBINE_INVITEES_2026.trim().split('\n').map(normalizeName));

const METRIC_DIRECTION = {
  fortyYard: 'lower',
  vertical: 'higher',
  broadJump: 'higher',
  threeCone: 'lower',
  shuttle: 'lower',
  benchPress: 'higher',
};

function computePercentile(value, sample, direction) {
  if (value == null || sample.length === 0) return null;
  if (sample.length === 1) return 100;
  const betterCount = sample.filter((candidate) => (
    direction === 'lower' ? candidate >= value : candidate <= value
  )).length - 1;
  const percentile = (betterCount / (sample.length - 1)) * 100;
  return Math.max(0, Math.min(100, Math.round(percentile)));
}

function mergeCombineData(player) {
  const key = normalizeName(player.name);
  const measurements = measurementsByPlayer.get(key);
  const testing = testingByPlayer.get(key);
  const isInvitee = inviteesByName.has(key);
  if (!measurements && !testing && !isInvitee) return player;

  return {
    ...player,
    combineInvite: isInvitee,
    combineSource: (measurements || testing) ? 'combine' : player.combineSource ?? null,
    combine: {
      ...player.combine,
      ...(measurements ?? {}),
      ...(testing ?? {}),
    },
    sources: {
      ...player.sources,
      ...(isInvitee ? { combineInviteList: NFL_COMBINE_INVITE_LIST_2026 } : {}),
      ...((measurements || testing) ? { combine: CBS_COMBINE_2026 } : {}),
    },
  };
}

function addCombinePercentiles(players) {
  return players.map((player) => {
    const peers = players.filter((candidate) => candidate.positionGroup === player.positionGroup);
    const nextPercentiles = { ...player.combinePercentiles };

    Object.entries(METRIC_DIRECTION).forEach(([metric, direction]) => {
      const sample = peers
        .map((candidate) => candidate.combine?.[metric])
        .filter((value) => value != null);
      nextPercentiles[metric] = computePercentile(player.combine?.[metric] ?? null, sample, direction);
    });

    return {
      ...player,
      combinePercentiles: nextPercentiles,
    };
  });
}

export function applyCombineData(players) {
  return addCombinePercentiles(players.map(mergeCombineData));
}

export function auditCombineInvitees(players) {
  const byName = new Map(players.map((player) => [normalizeName(player.name), player]));
  const unmatchedInvitees = COMBINE_INVITEES_2026.trim()
    .split('\n')
    .filter((name) => !byName.has(normalizeName(name)));

  const invitees = players.filter((player) => player.combineInvite);
  const tested = invitees.filter((player) => ['fortyYard', 'vertical', 'broadJump', 'threeCone', 'shuttle', 'benchPress']
    .some((metric) => player.combine?.[metric] != null));
  const measuredOnly = invitees.filter((player) => {
    const hasTesting = ['fortyYard', 'vertical', 'broadJump', 'threeCone', 'shuttle', 'benchPress']
      .some((metric) => player.combine?.[metric] != null);
    const hasMeasurements = player.combine?.heightIn != null || player.combine?.weightLbs != null;
    return hasMeasurements && !hasTesting;
  });
  const inviteOnly = invitees.filter((player) => {
    const hasMeasurements = player.combine?.heightIn != null || player.combine?.weightLbs != null;
    const hasTesting = ['fortyYard', 'vertical', 'broadJump', 'threeCone', 'shuttle', 'benchPress']
      .some((metric) => player.combine?.[metric] != null);
    return !hasMeasurements && !hasTesting;
  });

  return {
    totalInvitees: inviteesByName.size,
    matchedInvitees: invitees.length,
    unmatchedInvitees,
    tested: tested.length,
    measuredOnly: measuredOnly.length,
    inviteOnly: inviteOnly.length,
  };
}
